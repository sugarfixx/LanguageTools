 * @method public function convertCountryCode($tag, $format = 'lowercase')
 * @method private function iso2to3($tag, $format, $option)
 * @method private function iso3to2($tag, $format, $option)
 * @method private function alpha3to2($tag, $format)
 * @method private function alpha2to3($tag, $format)
 * @method private function returnFormated($value, $format)
 * @method private function iso2digit_T()
 * @method private function iso2digit_T()
