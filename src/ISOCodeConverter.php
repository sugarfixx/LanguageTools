class ISOCodeConverter
{
    /**
     * Convert ISO 639 formated language codes auto detects if input is 2 or 3 digit
     * @param $tag string
     * @param $format string
     * @param $option sets T or B
     * @return method
     */
    public function convertLanguageCode($tag, $format = FALSE, $option = FALSE )
    {
        $variant = ($option !== TRUE ? 'T' : 'B');
        if (strlen(trim($tag)) == 2 ) {
            return $this->iso2to3(trim($tag), $format, $variant );
        } elseif (strlen(trim($tag)) == 3) {
            return $this->iso3to2(trim($tag), $format, $variant);
        }
        return $tag.' is not a valid Language tag.';
    }

    /**
     * Converts ISO Alpha country codes auto detects if input is 2 or 3 digits
     * @param $tag string
     * @param $format string
     * @return method
     */
    public function convertCountryCode($tag, $format = FALSE)
    {
        if (strlen(trim($tag)) == 2) {
            return $this->alpha2to3(trim($tag),$format, $option);
        } elseif (strlen(trim($tag)) == 3) {
            return $this->alpha3to2(trim($tag),$format, $option);
        }
        return $tag.' is not a valid Country tag.';
    }

    /**
     * Finds ISO 639-1 formated language code and return the appropriate ISO 639-2/T or ISO 639-2/B code
     * @param $tag string
     * @param $format string
     * @param $option sets T or B
     * @return method
     */
    private function iso2to3($tag, $format, $option)
    {
        $method = 'iso2digit_'.$option;
        $dictionary = $this->{$method}();
        $value = $dictionary[strtolower($tag)];
        return $this->returnFormated($value, $format);
    }

    /**
     * Finds ISO 639-2/T ISO 639-2/B formated language code and return the appropriate ISO 639-1 code
     * @param $tag string
     * @param $format string
     * @param $option sets T or B
     * @return method
     */
    private function iso3to2($tag, $format, $option)
    {
        $method = 'iso3digit_'.$option;
        $dictionary = $this->{$method}();
        $value = $dictionary[strtolower($tag)];
        return $this->returnFormated($value, $format);
    }

    /**
     * Finds Alpha 3 formated country code and return the appropriate Alpha 2 code
     * @param $tag string
     * @param $format string
     * @return method
     */
    private function alpha3to2($tag, $format)
    {
        $dictionary = $this->alpha3digit();
        $value = $dictionary[strtolower($tag)];
        return $this->returnFormated($value, $format);
    }

    /**
     * Finds Alpha 2 formated country code and return the appropriate Alpha 3 code
     * @param $tag string
     * @param $format string
     * @return method
     */
    private function alpha2to3($tag, $format)
    {
        $dictionary = $this->alpha2digit();
        $value = $dictionary[strtolower($tag)];
        return $this->returnFormated($value, $format);
    }

    /**
     * Return result value string woth correct format
     * @param $value string
     * @param $format string
     * @return strtoupper||strtolower
     */
    private function returnFormated($value, $format)
    {
        return ($format !== TRUE ? strtolower($value) : strtoupper($value));
    }

